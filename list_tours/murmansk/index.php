 <b>Кировск</b><br>
					 Кировск – это маленький городок, окруженный горами Хибины (ранее он назывался Хибиногорск), находится на берегу озера Большой Вуъявр (в переводе с саамского языка «вудъявр» означает «горное озеро»).
				</p>
				<p>
 <b>Полярно-Альпийский сад </b><br>
					 Здесь можно познакомиться с представителями флоры различных стран, с особенностями их роста и развития в условиях, где в любой день лета возможны заморозки и снегопады. В питомниках и специальных экспозициях представлены коллекционные фонды растений ("Сад подснежников", "Каменистый сад", "Живой гербарий"). Ботанический сад предлагает посетителям экскурсии по питомникам открытого грунта, где представлены интродуцированные на Полярный север растения, в оранжерею тропических и субтропических растений, в музей истории и развития ботанического сада. Экскурсия по экологической тропе знакомит экскурсантов с растительностью разных высотных поясов Хибинских гор – от лесного до горно-тундрового поясов. Парковая территория открыта с июня по сентябрь. Посещение экологической тропы – в летнее время по специальным заявкам. Музей и оранжереи открыты в течение всего года.
				</p>
